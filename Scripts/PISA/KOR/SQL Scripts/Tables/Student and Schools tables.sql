CREATE SCHEMA IF NOT EXISTS pisa;

drop table if exists pisa.students; 
drop table if exists pisa.schools; 
CREATE TABLE pisa.schools (
    -- Identifiers
    CNT        CHAR(3)  NOT NULL,
    CNTSCHID   BIGINT   NOT NULL,

    -- Parent/guardian involvement (percentages: 0–100)
    SC064Q01TA SMALLINT CHECK (SC064Q01TA BETWEEN 0 AND 100),
    SC064Q02TA SMALLINT CHECK (SC064Q02TA BETWEEN 0 AND 100),
    SC064Q03TA SMALLINT CHECK (SC064Q03TA BETWEEN 0 AND 100),
    SC064Q04NA SMALLINT CHECK (SC064Q04NA BETWEEN 0 AND 100),
    SC064Q05WA SMALLINT CHECK (SC064Q05WA BETWEEN 0 AND 100),
    SC064Q06WA SMALLINT CHECK (SC064Q06WA BETWEEN 0 AND 100),
    SC064Q07WA SMALLINT CHECK (SC064Q07WA BETWEEN 0 AND 100),

    -- School characteristics
    SCHSIZE    INTEGER  CHECK (SCHSIZE BETWEEN 1 AND 30000),
    MCLSIZE    SMALLINT CHECK (MCLSIZE BETWEEN 5 AND 100),

    CONSTRAINT pk_schools
        PRIMARY KEY (CNT, CNTSCHID)
);


CREATE TABLE pisa.students (
    -- Identifiers
    CNT        CHAR(3)  NOT NULL,
    CNTSCHID   BIGINT   NOT NULL,
    CNTSTUID   BIGINT   NOT NULL,

    -- Derived / categorical covariates
    ST001D01T  SMALLINT CHECK (ST001D01T BETWEEN 7 AND 99),   -- grade
    ST004D01T  SMALLINT CHECK (ST004D01T IN (1, 2)),         -- gender
    IMMIG      SMALLINT CHECK (IMMIG BETWEEN 1 AND 3),      -- immigration status
    MISCED     SMALLINT CHECK (MISCED BETWEEN 1 AND 10),     -- ISCED

    -- Continuous indices
    AGE        NUMERIC(4,2) CHECK (AGE BETWEEN 10 AND 25),
    ESCS       NUMERIC(7,4) CHECK (ESCS BETWEEN -10 AND 10),

    -- SMS indicators (Likert: 1–4)
    ST268Q01JA SMALLINT CHECK (ST268Q01JA BETWEEN 1 AND 4),
    ST268Q04JA SMALLINT CHECK (ST268Q04JA BETWEEN 1 AND 4),
    ST268Q07JA SMALLINT CHECK (ST268Q07JA BETWEEN 1 AND 4),

    -- Mathematics plausible values (scores)
    PV1MATH    NUMERIC(8,3) CHECK (PV1MATH BETWEEN 0 AND 1000),
    PV2MATH    NUMERIC(8,3) CHECK (PV2MATH BETWEEN 0 AND 1000),
    PV3MATH    NUMERIC(8,3) CHECK (PV3MATH BETWEEN 0 AND 1000),
    PV4MATH    NUMERIC(8,3) CHECK (PV4MATH BETWEEN 0 AND 1000),
    PV5MATH    NUMERIC(8,3) CHECK (PV5MATH BETWEEN 0 AND 1000),
    PV6MATH    NUMERIC(8,3) CHECK (PV6MATH BETWEEN 0 AND 1000),
    PV7MATH    NUMERIC(8,3) CHECK (PV7MATH BETWEEN 0 AND 1000),
    PV8MATH    NUMERIC(8,3) CHECK (PV8MATH BETWEEN 0 AND 1000),
    PV9MATH    NUMERIC(8,3) CHECK (PV9MATH BETWEEN 0 AND 1000),
    PV10MATH   NUMERIC(8,3) CHECK (PV10MATH BETWEEN 0 AND 1000),

    CONSTRAINT pk_students
        PRIMARY KEY (CNT, CNTSCHID, CNTSTUID),

    CONSTRAINT fk_students_school
        FOREIGN KEY (CNT, CNTSCHID)
        REFERENCES pisa.schools (CNT, CNTSCHID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

